        <h4 class="my-5 text-info">Why is Forum important?</h4>
        <p>As a network of credible international volunteering and cooperation agencies with a long track record of
            experience, <b class=" text-info">Forum has a collective voice and unified identity in the values and
                principles that underpin
                volunteering for development</b>. Forum aims to share information, develop good practice and enhance
            cooperation
            and support between its members. Together, members explore innovative practice and research key contemporary
            issues, focusing on organisational learning and improved practice.</p>
        <p class="my-3"><b class=" text-info">Forum members celebrate and document the significance of volunteering and
                civil society
                inclusion through</b>: staying connected; sharing practices; celebrating diversity; promoting
            inclusiveness and
            reciprocity; sharing knowledge; having critical debates; influencing external stakeholders; and through
            learning and supporting each other within the volunteering community.</p>
        <h4 class="my-4 text-info">How is Forum resourced?</h4>
        <p class="my-3">

            Forum is resourced by membership subscriptions, occasional grants, and the voluntary staff time and funds
            from the organisations providing the Chair and the Forum Board of Directors. Subscriptions from members
            enable Forum to operate at a basic level with a part-time Executive Coordinator and the capacity to organise
            the annual IVCO conference.
        </p>
        <h4 class="my-4 text-info">Forum’s Origins</h4>
        <p class="my-3">
            Forum was founded in 1964 in Strasbourg under the auspices of the Council of Europe. Until 2000, Forum was
            an organisation for European-based agencies. In 2000, membership of Forum was opened up to organisations
            across the world. In 2002, Forum took over responsibility for running an annual meeting of Heads of
            International Volunteer Cooperation Organisations – now known as IVCO.
